class MyArray {
  constructor() {
    this.length = 0;
    this.data = {};
  }

  get(index) {
    return this.data[index];
  }

  push(item) {
    this.data[this.length] = item;
    this.length++;
    return this.length; // typical push method in JS returns the length
  }

  pop() {
    const lastItem = this.data[this.length - 1];
    delete this.data[this.length - 1];
    this.length--;
    return lastItem;
  }

  delete(index) {
    const item = this.data[index];
    this.shiftItems(index);

    return item;
  }

  shiftItems(index) {
    for (let i = index; i < this.length - 1; i++) {
      this.data[i] = this.data[i + 1];
    }

    delete this.data[this.length - 1];
    this.length--;
  }
}

/** Arrays in Javascript (unusual case)
 * are just objects with integer based keys that act like indexes-
 * different languages may have different implementations */
const newArray = new MyArray();
newArray.push('hi');
newArray.push('you');
newArray.push('!');
console.log(newArray);

console.log(newArray.pop());
